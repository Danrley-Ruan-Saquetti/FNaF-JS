function setup() {
    playSound(1, 15);
    contGame.alpha++;

    document.getElementById("start").addEventListener("click", () => {
        if (
            Math.round(document.getElementById("night").value) >= 1 &&
            Math.round(document.getElementById("night").value) <= 6
        ) {
            contGame.beta++;
            if (contGame.alpha == contGame.beta) {
                document.getElementById("menu").style.display = "none";
                document.getElementById("statistcs").style.display = "flex";
                document.getElementById("canvas").style.display = "flex";
                init();
            } else {
                contGame.beta = contGame.alpha;
            }
        } else {
            alert("Por favor, informe a night que deseja jogar!");
        }
    });
}

function init() {
    document.getElementById("statistcs").style.color = "#fff";
    document.getElementById("night-game").innerHTML = Math.round(
        document.getElementById("night").value
    );

    animateFrame = animateFrameNightEnd = null;
    songScreamWindow = controleSoundAnimaPorta = { bo: true, ch: true };
    controlFoxy = {
        corredor: { timeAtack: true, timeoutAtack: {} },
        runmode: { sound: true, atack: true },
    };
    controleComand = true;
    nightEndEfects = {
        victory: { a: 1, b: 0.01, c: 0 },
        powerDown: { a: 0, b: 0.01, c: 1 },
        jumpscare: { a: 0 },
    };
    intervals = {};
    controlSuperimposedComand = {
        lights: { e: false, d: false },
        camera: false,
    };
    consuption = { usage2: 0, usage3: 0, usage4: 0 };
    animatronics = new Animatronic();
    desk = new Desk(Math.round(document.getElementById("night").value));

    animatronics.activedifficulty();
    playSound(1, 10);
    playSound(2, 15);
    intervalo();
    animate();

    console.log(
        "Iniício da night " + Math.round(document.getElementById("night").value)
    );
    console.log("Horas: 0am");

    addEventListener("keydown", (ev) => {
        if (controleComand && desk.state == 0) {
            switch (ev.keyCode) {
                case 65:
                    MAP_COMANDS.doors.e();
                    break;
                case 68:
                    MAP_COMANDS.doors.d();
                    break;
                case 81:
                    MAP_COMANDS.lights.e();
                    break;
                case 69:
                    MAP_COMANDS.lights.d();
                    break;
                case 83:
                    MAP_COMANDS.camera();
                    break;
            }
            if (
                ev.keyCode == 65 ||
                ev.keyCode == 68 ||
                ev.keyCode == 81 ||
                ev.keyCode == 69 ||
                ev.keyCode == 83
            ) {
                desk.lastComand = ev.key;
                controleComand = false;
                let cont = 1;
                if (desk.lights.d) {
                    cont++;
                }
                if (desk.lights.e) {
                    cont++;
                }
                if (desk.doors.d) {
                    cont++;
                }
                if (desk.doors.e) {
                    cont++;
                }
                if (desk.camera) {
                    cont++;
                }
                desk.usage = cont;
                setTimeout(() => (controleComand = true), 500);
            }
        }
    });
}

function playSound(cod, sound) {
    let song, vol;

    switch (sound) {
        case 1:
            song = new Audio("Audio/Camera.mp3");
            vol = 0.3;
            break;
        case 2:
            song = new Audio("Audio/Close Door.mp3");
            vol = 0.25;
            break;
        case 3:
            song = SONG_CONTROL.lightOn;
            vol = 0.5;
            break;
        case 4:
            song = new Audio("Audio/Window scream.mp3");
            vol = 0.45;
            break;
        case 5:
            song = new Audio("Audio/Pirate sound.mp3");
            vol = 0.25;
            break;
        case 6:
            song = new Audio("Audio/Power Down.mp3");
            vol = 0.35;
            break;
        case 7:
            song = new Audio("Audio/Scream Jumpscare.mp3");
            vol = 0.3;
            break;
        case 8:
            song = new Audio("Audio/6am sound.mp3");
            vol = 0.5;
            break;
        case 9:
            song = SONG_CONTROL.foxyRun;
            vol = 0.5;
            break;
        case 10:
            song = SONG_CONTROL.ambienceSong;
            vol = 0.2;
            break;
        case 11:
            song = SONG_CONTROL.cameraMove;
            vol = 0.65;
            break;
        case 12:
            song = new Audio("Audio/Handle door.mp3");
            vol = 0.5;
            break;
        case 13:
            song = new Audio("Audio/Error.mp3");
            vol = 0.45;
            break;
        case 14:
            let i = Math.random() * 100 + 1;
            i < 33 ?
                (song = new Audio("Audio/Freddy's laugh 1.mp3")) :
                i < 66 ?
                (song = new Audio("Audio/Freddy's laugh 2.mp3")) :
                (song = new Audio("Audio/Freddy's laugh 3.mp3"));
            vol = 0.3;
            break;
        case 15:
            song = SONG_CONTROL.menuInit;
            vol = 0.45;
            break;
    }

    switch (cod) {
        case 1:
            if (sound == 3 || sound == 10 || sound == 11 || sound == 15) {
                song.loop = true;
                song.muted = false;
                if (sound == 10) SONG_CONTROL.cameraMove.muted = true;
                else if (sound == 11) SONG_CONTROL.ambienceSong.muted = true;
            }
            song.play();
            song.volume = vol;
            break;
        case 2:
            song.pause();
            break;
    }
}

function clearinterval() {
    clearInterval(intervals.a);
    clearInterval(intervals.b);
    clearInterval(intervals.c);
    clearInterval(intervals.d);
    clearTimeout(controlFoxy.corredor.timeoutAtack.a);
    clearTimeout(controlFoxy.corredor.timeoutAtack.b);
}

function intervalo() {
    intervals = {
        a: setInterval(() => {
            desk.time++;
            if (desk.time == 6) desk.state = 1;
            else if (animatronics.state.fo != 0)
                if (Math.random() * 100 + 1 < 4) playSound(1, 5);
            console.log("Horas: " + desk.time + "am");
        }, 20000),

        b: setInterval(() => {
            if (desk.usage == 4) {
                consuption.usage4++;
                consuption.usage3++;
                consuption.usage2++;
            }
            if (desk.usage == 3) {
                consuption.usage3++;
                consuption.usage2++;
            }
            if (desk.usage == 2) {
                consuption.usage2++;
            }

            if (animatronics.state.fr == 3) {
                if (desk.camera) {
                    animatronics.difficulty.fr.a = 0;
                    animatronics.difficulty.fr.b = 0;
                } else {
                    if (
                        animatronics.difficulty.fr.b > 0 &&
                        animatronics.difficulty.fr.b < 3
                    )
                        animatronics.difficulty.fr.b++;
                    if (animatronics.difficulty.fr.a == 9) {
                        animatronics.difficulty.fr.a = 0;
                        animatronics.difficulty.fr.b++;
                    } else {
                        if (animatronics.difficulty.fr.b == 0)
                            animatronics.difficulty.fr.a++;
                    }
                }
            }
        }, 1000),

        c: setInterval(() => {
            desk.battery -=
                consuption.usage2 * 0.5 +
                consuption.usage3 * 0.75 +
                consuption.usage4 +
                2;
            consuption = { usage2: 0, usage3: 0, usage4: 0 };
            if (desk.battery < 1) {
                desk.battery = 0;
                desk.state = 3;
            }
            console.log("Bateria: " + Math.round(desk.battery) + "%");
        }, 5000),

        d: setInterval(() => animatronics.update(), 3000),
    };
}

function eventSuperimposed() {
    if (controlSuperimposedComand.lights.e) {
        if (animatronics.state.bo == 3 && controleSoundAnimaPorta.bo) {
            playSound(1, 4);
            controleSoundAnimaPorta.bo = false;
            controlSuperimposedComand.lights.e = false;
        }
    }
    if (controlSuperimposedComand.lights.d) {
        if (animatronics.state.ch == 3 && controleSoundAnimaPorta.ch) {
            playSound(1, 4);
            controleSoundAnimaPorta.ch = false;
            controlSuperimposedComand.lights.d = false;
        }
    }
    if (controlSuperimposedComand.camera) {
        if (animatronics.state.fo == 2) {
            if (controlFoxy.runmode.sound) {
                if (desk.camera) {
                    animatronics.difficulty.fo.runmode = true;
                    animatronics.foxyRun();
                    playSound(1, 9);
                    controlFoxy.runmode.sound = false;
                    controlFoxy.runmode.atack = false;
                } else {
                    playSound(2, 9);
                }
            }
            controlSuperimposedComand.camera = false;
        }
    }
}

function animate() {
    animateFrame = requestAnimationFrame(animate);

    eventSuperimposed();

    desk.time == 0 ?
        (document.getElementById("timer").innerHTML = 12) :
        (document.getElementById("timer").innerHTML = desk.time);
    document.getElementById("battery").innerHTML = Math.round(desk.battery);

    for (let i = 2; i < 5; i++)
        desk.usage >= i ?
        (document.getElementById(`usage${i}`).style.display = "flex") :
        (document.getElementById(`usage${i}`).style.display = "none");

    if (desk.state != 0) {
        for (let i = 1; i < 5; i++)
            document.getElementById(`usage${i}`).style.display = "none";

        clearinterval();
        for (let i = 1; i < 13; i++) playSound(2, i);
        if (desk.state != 1) {
            if (desk.lights.d) desk.lights.d = false;
            if (desk.lights.e) desk.lights.e = false;
            if (desk.doors.d) {
                desk.doors.d = false;
                playSound(1, 2);
            }
            if (desk.doors.e) {
                desk.doors.e = false;
                playSound(1, 2);
            }
            if (desk.camera) {
                desk.camera = false;
                playSound(1, 1);
            }
        }
        switch (desk.state) {
            case 1:
                playSound(1, 8);
                console.log("Parabéns! Você ganhou!");
                setTimeout(() => {
                    ANIMATE_NIGHT_END.victory();
                    let j = setInterval(() => {
                        nightEndEfects.victory.c++;
                        if (nightEndEfects.victory.c == 11) clearInterval(j);
                    }, 1000);
                }, 1000);
                break;
            case 2:
                playSound(1, 7);
                console.log("Você morreu! Game Over!");
                ANIMATE_NIGHT_END.jumpscare();
                let l = setInterval(() => {
                    nightEndEfects.jumpscare.a++;
                    if (nightEndEfects.jumpscare.a == 7) clearInterval(l);
                }, 1000);
                break;
            case 3:
                playSound(1, 6);
                console.log("A bateria acabou! Game Over!");
                ANIMATE_NIGHT_END.powerDown();
                let k = setInterval(() => {
                    nightEndEfects.powerDown.a++;
                    if (nightEndEfects.powerDown.a == 10) clearInterval(k);
                }, 1000);
                break;
        }
        cancelAnimationFrame(animateFrame);
    }

    ctx.fillStyle = "#000";
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    if (!desk.camera) {
        BLOCKS.escritorioBlocos.forEach((map, i) => {
            map.forEach((bloco, j) => {
                bloco.color.state = bloco.color.origin;
                if (desk.lights.e) {
                    if (bloco.code == 1 || bloco.code == 3)
                        bloco.color.state = "#797979";
                    else if (bloco.code == 5) bloco.color.state = "#006eff";
                }
                if (desk.lights.d) {
                    if (bloco.code == 2 || bloco.code == 4)
                        bloco.color.state = "#797979";
                    else if (bloco.code == 6) bloco.color.state = "#006eff";
                }
                if (desk.doors.e) {
                    if (bloco.code == 1)
                        i < BLOCKS.escritorioBlocos.length - 3 ?
                        (bloco.color.state = "#353535") :
                        (bloco.color.state = "#ff4141");
                    else if (bloco.code == 7) bloco.color.state = "#ff0000";
                }
                if (desk.doors.d) {
                    if (bloco.code == 2)
                        i < BLOCKS.escritorioBlocos.length - 3 ?
                        (bloco.color.state = "#353535") :
                        (bloco.color.state = "#ff4141");
                    else if (bloco.code == 8) bloco.color.state = "#ff0000";
                }

                bloco.draw();
            });
        });

        if (desk.doors.e)
            BLOCKS.detalhedoorsBlocos.e.forEach((detalhe) => detalhe.update());
        if (desk.doors.d)
            BLOCKS.detalhedoorsBlocos.d.forEach((detalhe) => detalhe.update());

        animatronics.draw();
    } else {
        BLOCKS.cameraBlocos.forEach((map, i) =>
            map.forEach((bloco, j) => bloco.draw())
        );

        animatronics.draw();
        BLOCKS.chiassoBlocos.forEach((chiasso) => chiasso.update());
    }
}

